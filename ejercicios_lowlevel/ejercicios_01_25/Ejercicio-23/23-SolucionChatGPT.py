'''
23. CÁLCULO DE LA LONGITUD DE UNA CADENA DE TEXTO

Escribir un programa que pida al usuario una cadena de texto y muestre en pantalla su longitud, es decir, el número de caracteres que tiene la cadena.
'''

# Primero, se pide al usuario que ingrese la cadena de texto mediante la función input().  
texto = input("Ingrese una cadena de texto: ")
# Luego, se calcula su longitud utilizando la función len() y se almacena en la variable longitud.
longitud = len(texto)
# Finalmente, se imprime en pantalla el mensaje con la longitud utilizando la función print().
print("La longitud de la cadena es:", longitud)


