'''
08. ÁREA DE UN TRIÁNGULO

Escribir un programa que pida al usuario la base y la altura de un triángulo y muestre en pantalla el área del triángulo, utilizando la fórmula A = (b * h) / 2.
'''

# Pedimos al usuario que ingrese la base y la altura del triángulo
base = float(input("Ingrese la base del triángulo: "))
altura = float(input("Ingrese la altura del triángulo: "))

# Calculamos el área del triángulo utilizando la fórmula dada
area = (base * altura) / 2

# Mostramos el área del triángulo por pantalla
print("El área del triángulo es:", area)


